"""
Помилки (номери рядків через пробіл, цей рядок - №2): 7 9 11
"""

def sum_of_digits(n):
    """Повернути суму цифр менших 5 для позитивного цілого числа `n`.
    Якщо таких цифр немає, повернути 0."""
    c = 0
    while n > 0:
        digit = n % 10
        if digit < 5:
            c = c + digit
        n //= 10
    return c

print(sum_of_digits(123))
